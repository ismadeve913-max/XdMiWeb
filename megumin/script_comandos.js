document.addEventListener('DOMContentLoaded', () => {
    const botonesContainer = document.getElementById('botones-container');
    const panelContainer = document.getElementById('command-interface-container');
    const panelContent = document.getElementById('dynamic-panel-content');
    const closeButton = document.getElementById('close-panel');
    const searchInput = document.getElementById('command-search');
    const loadingOverlay = document.getElementById('loading-overlay');
    const rotatingImage = document.getElementById('rotating-image');

    // Inicializar la animación de carga
    setTimeout(() => {
        loadingOverlay.classList.add('hidden');
    }, 2000);

    // Activar la animación de giro en la imagen principal al cargar
    rotatingImage.classList.add('spin-active');

    // Generar los botones del menú
    function generateButtons(data) {
        botonesContainer.innerHTML = '';
        for (const category in data) {
            const categoryTitle = document.createElement('h2');
            categoryTitle.className = 'category-title';
            categoryTitle.textContent = category.replace(/ /g, ' ').toUpperCase();
            botonesContainer.appendChild(categoryTitle);

            data[category].forEach(commandText => {
                const button = document.createElement('button');
                button.className = 'boton-pagina';
                button.textContent = commandText;
                button.onclick = () => showCommandInfo(commandText);
                botonesContainer.appendChild(button);
            });
        }
    }

    // Mostrar el panel de información del comando
    function showCommandInfo(command) {
        const description = commandDescriptions[command] || "Descripción no disponible para este comando.";
        panelContent.innerHTML = `
            <h2>Comando: ${command}</h2>
            <p>${description}</p>
        `;
        panelContainer.classList.add('active');
        panelContainer.classList.add('glowing-border');
    }

    // Cerrar el panel
    closeButton.onclick = () => {
        panelContainer.classList.remove('active');
        panelContainer.classList.remove('glowing-border');
    };

    // Filtrar comandos en tiempo real
    searchInput.addEventListener('input', (e) => {
        const searchTerm = e.target.value.toLowerCase();
        const filteredData = {};

        for (const category in commandData) {
            const filteredCommands = commandData[category].filter(command => 
                command.toLowerCase().includes(searchTerm)
            );
            if (filteredCommands.length > 0) {
                filteredData[category] = filteredCommands;
            }
        }
        generateButtons(filteredData);
    });

    // Generar los botones iniciales
    generateButtons(commandData);
});
